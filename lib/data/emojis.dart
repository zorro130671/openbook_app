/// Our default selection (tweak this anytime)
const List<String> defaultPackIds = [
  'party', 'nature', 'love', 'tech', 'travel', 'sports', 'foodSweet',
  // 'spooky', // add seasonally
  // 'gaming','work', // add if you want
];

/// Build the final emoji list.
/// - [include]   pack ids to include (defaults to [defaultPackIds])
/// - [exclude]   pack ids to exclude
/// - [shuffle]   randomize the order
/// - [limit]     cap the list length
/// - [base]      base emoji set to prepend (deduping applied)
List<String> buildEmojis({
  List<String>? include,
  List<String>? exclude,
  bool shuffle = false,
  int? limit,
  List<String> base = baseEmojis,
}) {
  final ids = include ?? defaultPackIds;
  final blocked = (exclude ?? const <String>[]).toSet();

  final out = <String>[];
  final seen = <String>{};

  void addAll(List<String> src) {
    for (final e in src) {
      if (seen.add(e)) out.add(e); // dedupe, keep first occurrence
    }
  }

  // base first
  addAll(base);

  // then packs
  for (final id in ids) {
    if (blocked.contains(id)) continue;
    final pack = _allPacks[id];
    if (pack != null) addAll(pack);
  }

  if (shuffle) out.shuffle();
  if (limit != null && out.length > limit) {
    return out.sublist(0, limit);
  }
  return out;
}
